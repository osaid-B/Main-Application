export function normalizeArabicText(value: string) {
  let next = EXACT_REPLACEMENTS[value] ?? value;

  for (const [search, replacement] of SUBSTRING_REPLACEMENTS) {
    next = next.replaceAll(search, replacement);
  }

  return next;
}

export function normalizeArabicRecord(record: Record<string, string>) {
  return Object.fromEntries(
    Object.entries(record).map(([key, value]) => [key, normalizeArabicText(value)])
  );
}

export function normalizeArabicTree<T>(value: T): T {
  if (typeof value === "string") {
    return normalizeArabicText(value) as T;
  }

  if (Array.isArray(value)) {
    return value.map((item) => normalizeArabicTree(item)) as T;
  }

  if (value && typeof value === "object") {
    return Object.fromEntries(
      Object.entries(value).map(([key, item]) => [key, normalizeArabicTree(item)])
    ) as T;
  }

  return value;
}
